class User:
    def __init__(self, user_data:dict):
        self.username = user_data['username']
        self.email = user_data['email']
        self.title = user_data['title']
        self.password = user_data['password']
        self.dob_day = user_data['dob_day']
        self.dob_month = user_data['dob_month']
        self.dob_year = user_data['dob_year']
        self.first_name = user_data['first_name']
        self.last_name = user_data['last_name']
        self.company = user_data['company']
        self.address1 = user_data['address1']
        self.address2 = user_data['address2']
        self.country = user_data['country']
        self.state = user_data['state']
        self.city = user_data['city']
        self.zipcode = user_data['zipcode']
        self.mobile_number = user_data['mobile_number']
